clc; 
clf; 
clear all; 
close all; 
am=input('ENTER THE AMPLITUDE,am='); 
fm=input('enter the frequency,fm='); 
cy=input('enter the number of cycles,cy='); 
t=0:(1/(fm*fm)):cy/fm; 
amp=am*sin(2*pi*fm*t); 
subplot(221); 
plot(t,amp); 
grid on; 
xlabel('time'); 
ylabel('amplitude'); 
title('sine wave'); 
subplot(222); 
fs1=1.5*fm; 
t1=0:(1/(fs1)):cy/fm; 
amp1=am*sin(2*pi*fm*t1); 
plot(t1,amp1); 
grid on; 
xlabel('time'); 
ylabel('amplitude');
title('under sampling'); 
subplot(223); 
fs2=20*fm; 
t2=0:(1/(fs2)):cy/fm; 
amp2=am*sin(2*pi*fm*t2); 
plot(t2,amp2); 
grid on; 
xlabel('time'); 
ylabel('amplitude'); 
title('oversampling'); 
subplot(224); 
